#include "libft.h"

/*
**	Description
**	-----------
**	Counts the number of elements in a linked list.
**
**	Parameters
**	----------
**	t_list *lst: the head of the list.
**
**	Returns
**	-------
**	The lenght of the linked list.
*/

int	ft_lstsize(t_list *lst)
{
	size_t i;

	i = 0;
	while (lst != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
